CREATE TABLE CC_TRANSLATORS
(
  VALUE_IN     VARCHAR2(10)                NOT NULL,
  VALUE_OUT    VARCHAR2(10)                NOT NULL,
  ORDER_NUMB   NUMBER,
  CREATE_USER  VARCHAR2(30),
  CREATE_DATE  DATE,
  MODIFY_USER  VARCHAR2(30),
  MODIFY_DATE  DATE
)
/


CREATE OR REPLACE TRIGGER CCTR_BU 
 BEFORE UPDATE 
 ON  CC_TRANSLATORS 
 REFERENCING OLD AS OLD NEW AS NEW 
 FOR EACH ROW 
-- PL/SQL Block
BEGIN 
 :new.MODIFY_USER := substr(user,1,30); 
 :new.MODIFY_DATE := sysdate; 
END;
/



CREATE OR REPLACE TRIGGER CCTR_BI 
 BEFORE INSERT 
 ON CC_TRANSLATORS 
 REFERENCING OLD AS OLD NEW AS NEW 
 FOR EACH ROW 
-- PL/SQL Block
BEGIN 
 if :new.CREATE_USER IS NULL THEN 
   :new.CREATE_USER := substr(user,1,30); 
 END IF; 
 :new.CREATE_DATE := sysdate; 
END;
/



