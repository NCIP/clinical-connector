CREATE TABLE STUDY_PRIVATE_CDE
(
  STUDY          VARCHAR2(15)              NOT NULL,
  CDE_ID         VARCHAR2(40)              NOT NULL,
  CDE_VERSION    VARCHAR2(30)              NOT NULL,
  CREATE_USER    VARCHAR2(30),
  CREATE_DATE    DATE,
  MODIFY_USER    VARCHAR2(30),
  MODIFY_DATE    DATE
)
/


CREATE INDEX SPC_INDX ON STUDY_PRIVATE_CDE
(STUDY)
/


CREATE OR REPLACE TRIGGER SPC_BI 
 BEFORE INSERT 
 ON STUDY_PRIVATE_CDE 
 REFERENCING OLD AS OLD NEW AS NEW 
 FOR EACH ROW 
-- PL/SQL Block
BEGIN 
 if :new.CREATE_USER IS NULL THEN 
   :new.CREATE_USER := substr(user,1,30); 
 END IF; 
 :new.CREATE_DATE := sysdate; 
END;
/


CREATE OR REPLACE TRIGGER SPC_BU 
 BEFORE UPDATE 
 ON STUDY_PRIVATE_CDE 
 REFERENCING OLD AS OLD NEW AS NEW 
 FOR EACH ROW 
-- PL/SQL Block
BEGIN 
 :new.MODIFY_USER := substr(user,1,30); 
 :new.MODIFY_DATE := sysdate; 
END;
/


